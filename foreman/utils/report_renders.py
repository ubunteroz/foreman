def render_rtf_report(case, histories, render_file=None):
    if render_file is None:
        render_file = StringIO()

    if rtf_import:
        dr = Renderer()
        doc = create_rtf(case, histories)
        dr.Write(doc, render_file)
        return render_file
    else:
        return None